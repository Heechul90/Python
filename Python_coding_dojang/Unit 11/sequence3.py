## 11.3.3  마지막 요소에 접근하기
a = [38, 21, 53, 62, 19]
print(len(a))    # 리스트의 길이를 구함
# print(a[5])    # 리스트의 길이를 인덱스로 지정
# print(a[len(a)])

print(a[len(a) - 1])    # 마지막 요소(인덱스 4) 출력


