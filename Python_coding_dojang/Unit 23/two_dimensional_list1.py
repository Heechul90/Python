# 참고 | 2차원 튜플
# 튜플 = ((값, 값), (값, 값), (값, 값))
# 튜플 = ([값, 값], [값, 값], [값, 값])
# 리스트 = [(값, 값), (값, 값), (값, 값)]

a = ((10, 20), (30, 40), (50, 60))    # 튜플 안에 튜플을 넣은 2차원 튜플
b = ([10, 20], [30, 40], [50, 60])    # 튜플 안에 리스트를 넣음
c = [(10, 20), (30, 40), (50, 60)]    # 리스트 안에 튜플을 넣음
print(a)
print(b)
print(c)

