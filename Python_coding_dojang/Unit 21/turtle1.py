### Unit 21. 터틀 그래픽스로 그림 그리기
## 21.1 사각형 그리기
import turtle as t
t.shape('turtle')

t.forward(100)   # 거북이를 100픽셀만큼 앞으로(forward) 이동
t.right(90)      # 거북이의 방향을 오른쪽(right)으로 90도 회전
t.forward(100)   # 거북이를 100픽셀만큼 앞으로(forward) 이동
t.right(90)      # 거북이의 방향을 오른쪽(right)으로 90도 회전
t.forward(100)   # 거북이를 100픽셀만큼 앞으로(forward) 이동
t.right(90)      # 거북이의 방향을 오른쪽(right)으로 90도 회전
t.forward(100)   # 거북이를 100픽셀만큼 앞으로(forward) 이동
t.mainloop()     # 터틀 창이 종료될 때까지 마우스, 키보드 입력을 대기

# 앞으로 이동: forward, fd
# 뒤로 이동: backward, bk, back
# 왼쪽으로 회전: left, lt
# 오른쪽으로 회전: right, rt

